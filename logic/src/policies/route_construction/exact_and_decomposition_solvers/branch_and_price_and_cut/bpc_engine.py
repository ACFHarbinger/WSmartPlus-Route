r"""
Branch-and-Price-and-Cut (BPC) Engine for VRPP.

This engine implements a state-of-the-art exact solver for the Vehicle Routing
Problem with Profits (VRPP), adapting the macro-architectural sequencing
framework of Barnhart, Hane, and Vance (2000) ["BHV2000"] to modern routing
heuristics and the Set Partitioning (SP) master formulation.

Paper-to-Implementation Mapping (BHV2000)
==========================================

§2 – ODIMCF Formulation → VRPP Set Partitioning
    Paper formulates arc-based multi-commodity flow (ODIMCF); this engine
    uses an equivalent column-based Set Partitioning Problem (SPP) over
    route variables λ_k.  Customer coverage constraints replace O-D flow
    conservation; the arc-capacity knapsack structure underpins LCI cuts.
    See: ``VRPPMasterProblem`` in master_problem.py.

§3 – Column Generation / Restricted Master Problem (RMP)
    LP relaxation of the SPP is solved iteratively.  New columns (routes)
    are generated by the RCSPP pricing subproblem (see §3.2 below).
    Phase I (Farkas pricing) guarantees initial LP feasibility without Big-M.
    Phase II switches to profit-maximising pricing once feasibility holds.
    Implementation: ``_column_generation_loop``, ``_solve_farkas_pricing_step``,
    ``_solve_pricing_step``.

§3.2 – Pricing Subproblem (RCSPP)
    Paper uses a shortest-path labelling algorithm on a time-space network.
    This engine uses a label-correcting DP with ng-route relaxation
    (Baldacci et al. 2011) to avoid the worst-case exponential state space.
    Reduced-cost formula: c'_k = c_k − Σ_i π_i δ_ik − Σ π_S · a_Sk
                                      − Σ γ_{lm} · α_{lm}^k  (LCI §4.2)
    Implementation: ``RCSPPSolver`` / ``_label_correcting_algorithm`` in
    rcspp_dp.py.

§4.1 – Lifted Cover Inequalities (LCI)
    Arc-capacity knapsack cuts lifted via the Gu, Nemhauser, Savelsbergh
    (1995a) exact sequence-independent procedure.  Both the physical-capacity
    engine (PhysicalCapacityLCIEngine) and the saturated-arc engine
    (SaturatedArcLCIEngine) are implemented; the latter matches §6 exactly.
    LCI duals γ_{lm} modify pricing arc costs as c'_{lm}^k = c_{lm}^k + γ_{lm}·α_{lm}^k.
    Implementation: cutting_planes.py; dual integration: rcspp_dp.py:
    ``_extend_label``.

§4.2 – LCI Dual Integration in Pricing
    Per-node α lifting coefficients are archived in the GlobalCutPool and
    re-injected at descendant B&B nodes via ``apply_to_master``.  The
    composite dual dict emitted by ``get_reduced_cost_coefficients`` includes
    ``lci_duals`` and ``lci_node_alphas`` keys, which flow through to the
    label-extension step.
    Implementation: master_problem.py: ``get_reduced_cost_coefficients``;
    rcspp_dp.py: ``solve`` → ``_label_correcting_algorithm`` → ``_extend_label``.

§4.3 – Divergence Node Branching
    Two highest-λ fractional routes are traced step-by-step from the depot.
    The first node where they diverge is the branching node d.  arc_set_1
    contains the arc of the longer-remaining-path route (child 1 is explored
    first in DFS — better basis warm-start).  arc_set_2 covers all other
    outgoing arcs from d.  Falls back to aggregate-flow heuristic when no
    common prefix exists.
    Implementation: ``MultiEdgePartitionBranching.find_divergence_node``
    in branching.py.

§5 – DFS Node Selection
    The B&B tree uses Depth-First Search (``DepthFirstStrategy``) to maximise
    LP basis reuse across parent → child transitions.  A warm-start snapshot
    of the parent's LP basis is stored in ``BranchNode.lp_basis`` and
    restored via ``master.restore_basis`` at the start of each child's CG loop.
    Implementation: ``search_strategy.py``, ``BranchAndBoundTree.add_node``,
    ``_column_generation_loop`` (``parent_basis`` arg).

§6 – Saturated-Arc LCI Separation
    For each arc (i,j) with fractional flow > 1 − ε, the set of routes using
    that arc forms a cover; the basic cover inequality is lifted and added.
    Implementation: ``SaturatedArcLCIEngine`` in cutting_planes.py.

Table 2 – CG at Root Only
    Set ``BPCParams.cg_at_root_only = True`` to replicate the Table 2
    experiment: column generation is skipped at all non-root nodes; only
    the pool built at the root is sifted and the LP re-solved.
    Implementation: ``_column_generation_loop`` (``cg_at_root_only`` arg).

Additional components not in BHV2000 (VRPP adaptations):
    - Ryan-Foster (1981) node-pair branching for the SPP.
    - Rounded Capacity Cuts (RCC, Lysgaard et al. 2004).
    - Subset-Row Inequalities (3-SRI, Jepsen et al. 2008).
    - Dual smoothing (Wentges 1997) for faster CG convergence.
    - GlobalCutPool: centralised cut registry ensuring parent cuts are
      re-injected at all descendant B&B nodes.

References:
-----------
- Barnhart, C., Hane, C. A., & Vance, P. H. (2000). "Using branch-and-price-and-cut
  to solve origin-destination integer multicommodity flow problems."
  Operations Research, 48(2), 318–326.
- Barnhart, C., et al. (1998). "Branch-and-price: Column generation for solving
  huge integer programs." Operations Research, 46(3), 316–329.
- Baldacci, R., et al. (2011). "New state-space relaxation for the car-dependent VRP."
- Ryan, D. M., & Foster, B. A. (1981). "An integer programming approach to scheduling."
- Lysgaard, J., et al. (2004). "A new branch-and-cut algorithm for the capacitated VRP."
- Jepsen, M., et al. (2008). "Subset-row inequalities applied to the VRP with time windows."
- Gu, Z., Nemhauser, G. L., & Savelsbergh, M. W. P. (1995a). "Lifted cover inequalities
  for 0-1 integer programs."
- Wentges, P. (1997). "Weighted Dantzig-Wolfe decomposition for linear mixed-integer programming."
"""

